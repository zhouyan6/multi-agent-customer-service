def build_cases():
    """组装所有测试用例并写入 JSONL。"""
    cases = []

    domain_map = [
        ("products", "product_info", PRODUCT_CASES, "prod"),
        ("tech_support", "technical_support", TECH_CASES, "tech"),
        ("billing", "billing", BILLING_CASES, "bill"),
        ("complaints", "complaint", COMPLAINT_CASES, "comp"),
        ("general_service", "general_inquiry", GENERAL_CASES, "gen"),
    ]

    for domain, qtype, raw_list, prefix in domain_map:
        assert len(raw_list) >= 40, f"{domain} 期望至少 40 条，实际 {len(raw_list)} 条"
        raw_list = raw_list[:40]
        for i, c in enumerate(raw_list, start=1):
            cases.append({
                "id": f"{prefix}_{i:03d}",
                "domain": domain,
                "query": c["q"],
                "expected_query_type": qtype,
                "expected_keywords": c["kw"],
                "expected_answer_keywords": c["ans"],
                "is_out_of_scope": False,
            })

    assert len(OOS_CASES) >= 30, f"越界用例期望至少 30 条，实际 {len(OOS_CASES)} 条"
    oos_list = OOS_CASES[:30]
    for i, c in enumerate(oos_list, start=1):
        cases.append({
            "id": f"oos_{i:03d}",
            "domain": "out_of_scope",
            "query": c["q"],
            "expected_query_type": "out_of_scope",
            "expected_keywords": [],
            "expected_answer_keywords": [],
            "is_out_of_scope": True,
            "oos_category": c["cat"],
        })

    return cases


